using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;

public static class Codex_MathLib
{
    public static long square(long x)
    {
        return (x * x);
    }

    public static long cube(long x)
    {
        return ((x * x) * x);
    }

    public static long helper(long x)
    {
        return (x + 1L);
    }

}
